//! Interactive setup wizard that writes a `.env` file.

use std::fs;
use std::io::{self, BufRead, Write};
use std::path::PathBuf;

fn ask(prompt: &str, default: &str) -> String {
    let label = if default.is_empty() {
        format!("{prompt}: ")
    } else {
        format!("{prompt} [{default}]: ")
    };
    print!("{label}");
    io::stdout().flush().ok();
    let mut line = String::new();
    let _ = io::stdin().lock().read_line(&mut line);
    let trimmed = line.trim();
    if trimmed.is_empty() { default.to_string() } else { trimmed.to_string() }
}

#[derive(Debug, Clone)]
struct Provider {
    name: &'static str,
    default_url: &'static str,
}

fn provider_for(choice: &str) -> (&'static str, Provider) {
    match choice {
        "1" => ("lmstudio", Provider { name: "LM Studio", default_url: "http://localhost:1234/v1" }),
        "2" => ("ollama", Provider { name: "Ollama", default_url: "http://localhost:11434/v1" }),
        "3" => ("openrouter", Provider { name: "OpenRouter", default_url: "https://openrouter.ai/api/v1" }),
        "4" => ("openai", Provider { name: "OpenAI", default_url: "https://api.openai.com/v1" }),
        _ => ("custom", Provider { name: "Custom", default_url: "" }),
    }
}

fn main() {
    println!("\n  ⚡ itsy Setup\n  ─────────────────────────────────\n");
    println!("  Providers:");
    println!("    1) LM Studio (local, default port 1234)");
    println!("    2) Ollama (local, default port 11434)");
    println!("    3) OpenRouter (cloud, needs API key)");
    println!("    4) OpenAI (cloud, needs API key)");
    println!("    5) Custom endpoint");
    println!();

    let choice = ask("  Choose provider (1-5)", "1");
    let (provider_id, provider) = provider_for(&choice);

    let mut base_url = provider.default_url.to_string();
    if provider_id == "custom" || base_url.is_empty() {
        base_url = ask("  Base URL (e.g. http://host:port/v1)", "http://localhost:1234/v1");
    } else {
        let custom = ask("  Base URL", &base_url);
        if !custom.is_empty() {
            base_url = custom;
        }
    }

    let model_name = match provider_id {
        "openrouter" => ask("  Model (e.g. openai/gpt-4o-mini, google/gemma-2-9b-it)", "openai/gpt-4o-mini"),
        "openai" => ask("  Model", "gpt-4o-mini"),
        _ => ask("  Model name (as shown in your server)", ""),
    };

    let mut api_key = String::new();
    if provider_id == "openrouter" || provider_id == "openai" {
        api_key = ask("  API Key", "");
        if api_key.is_empty() {
            println!("  ⚠ No API key. Set OPENAI_API_KEY in .env later.");
        }
    } else {
        let needs = ask("  Does your server require an API key? (y/N)", "n");
        if needs.to_lowercase() == "y" {
            api_key = ask("  API Key", "");
        }
    }

    let mut lines = vec![
        "# itsy configuration (generated by itsy init)".to_string(),
        format!("ITSY_MODEL={model_name}"),
        format!("ITSY_BASE_URL={base_url}"),
        "ITSY_PROVIDER=openai".to_string(),
    ];
    if !api_key.is_empty() {
        lines.push(format!("OPENAI_API_KEY={api_key}"));
    }
    lines.push(String::new());

    let path: PathBuf = std::env::current_dir().unwrap_or_else(|_| PathBuf::from(".")).join(".env");
    if path.exists() {
        println!("\n  ⚠ .env already exists. Overwriting (Ctrl+C to cancel).");
    }
    if let Err(e) = fs::write(&path, lines.join("\n")) {
        eprintln!("  ✗ Failed to write {}: {e}", path.display());
        return;
    }
    println!("\n  ✓ Created {}", path.display());
    println!("\n  Run `itsy` to start!\n");
}
